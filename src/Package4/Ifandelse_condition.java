package Package4;

public class Ifandelse_condition {

	public static void main(String[] args) {
		// if 
		int age = 3;
		if (age >= 18) {
			System.out.println("Eligible and age is "+age);
		}
		if (age >= 18) {
			System.out.println("Eligible and age is "+age);
		}
		else
		{
			System.out.println("Not eligible and age is "+age);
		}
		// Odd or even
		int a = 5;
		if (a%2==0)
		{
			System.out.println("It's even number");
		}
		else {
			System.out.println("It's an odd number");
		}

	}

}
